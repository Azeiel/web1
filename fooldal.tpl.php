				<h1 >Gyermekmentő Alapítvány - 16 ÉVE Önökkel</h1>
				<br>
				<p id="bekezdes1">A Gyermekmentő Támogató Alapítvány azzal a céllal jött létre 2004-ben, hogy a gyógyíthatatlan, súlyos beteg, vagy rossz anyagi háttérrel rendelkező gyermekek, szülők, kórházak részére nyújtson segítséget. Igyekszünk egyre nagyobb hangsúlyt fektetni a megelőzésre. Elsődleges célunkká vált megelőzni a betegségeket nem csak gyógyítani.</p>
				<br>
				<h3 ><strong>Balesetmegelőzés kisgyermekkorban</strong></h3>  <br>
				<br>
				<p id="bekezdes1">A gyermekhalálozás a felére csökkent az elmúlt 15 évben Magyarországon, azonban még mindig kétszer annyi gyermek hal meg balesetben, mint onkohematológiai okok miatt. Pedig a baleseti halálok fele, az ebből eredő sérülések 80 százaléka megelőzhető lenne.</p>
				
				<p id="bekezdes1">Nagyobb hangsúlyt kell fektetni a megelőzésre, elsőként a lakosság oktatására hogy képesek legyenek felismerni, és ha lehet, elkerülni a veszélyt, tudjanak elsősegélyt nyújtani, segítséget hívni. Imerjék és hazsnálják a prevenció eszközeit a mindennapok során.</p>
				
				<p id="bekezdes1">Kisgyermekkorban (1 és 3 év között) a balesetek igen gyakoriak. A kisgyerek gyorsan, sokszor hirtelen fejlődik. Olyasmire is képes lesz egyik napról a másikra,amire korábban nem gondoltunk volna.</p>
				
				<p id="bekezdes1">Hajtja a kíváncsiság, de még nem teljesen ura saját mozgásának. Ez közvetlen vészhelyzetet teremthet: a kicsi leeshet, résekbe beszorulhat. Egyszer csak elér olyan dolgokat, amelyeket korábban nem, és azt gondoltuk, hogy azok nem is veszélyeztetik. A kisgyereknek meg kell tapasztalnia, fel kell fedeznie a világot. A mi felelősségünk, hogy ez biztonságos környezetben történjen.</p>
